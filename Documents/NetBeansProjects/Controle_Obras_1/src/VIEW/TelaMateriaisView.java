private void editarNoBanco(String codigoItemAntigo, String novoCodigoItem, String nomeItem, String uniMedida) {
    Connection conn = null;
    PreparedStatement stmt = null;

    try {
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetoestoque?serverTimezone=America/Sao_Paulo", "root", "1234");
        
        // ComeÃ§a a construÃ§Ã£o da SQL
        StringBuilder sql = new StringBuilder("UPDATE projetoestoque.materiais_estoque SET ");
        List<String> updates = new ArrayList<>();

        // Adiciona apenas os campos que nÃ£o sÃ£o nulos
        if (novoCodigoItem != null) updates.add("CÃ³digoItem = ?");
        if (nomeItem != null) updates.add("NomeItem = ?");
        if (uniMedida != null) updates.add("UniMedida = ?");
        
        // Verifica se algum campo foi passado para atualizaÃ§Ã£o
        if (updates.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum campo para atualizar.");
            return;
        }

        // Adiciona os updates Ã  SQL
        sql.append(String.join(", ", updates));
        sql.append(" WHERE CÃ³digoItem = ?"); // CondiÃ§Ãµes para identificar o registro

        stmt = conn.prepareStatement(sql.toString());

        // Preenche os parÃ¢metros
        int index = 1;
        
        if (novoCodigoItem != null) stmt.setString(index++, novoCodigoItem);
        if (nomeItem != null) stmt.setString(index++, nomeItem);
        if (uniMedida != null) stmt.setString(index++, uniMedida);
        
        // Adiciona o parÃ¢metro de filtro
        stmt.setString(index++, codigoItemAntigo); // Use o cÃ³digo antigo para a condiÃ§Ã£o WHERE
        
        // Execute a atualizaÃ§Ã£o
        stmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Registro editado com sucesso!");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao editar registro no banco.");
    } finally {
        try {
            if (stmt != null) stmt.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
}


