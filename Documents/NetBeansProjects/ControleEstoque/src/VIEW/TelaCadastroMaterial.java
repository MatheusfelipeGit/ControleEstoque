    private void CadastroNovoMaterialBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastroNovoMaterialBtnActionPerformed
        
        try{
            
        String Nomei, UniMedida;
        // Alterado para double

    
        Nomei = NomeItemTxt.getText();
        UniMedida = (String) UniMedidaBox.getSelectedItem();
            
            
            
            MateriaisDTO objmateriaisDto = new MateriaisDTO();
            
            objmateriaisDto.setNomeItem(Nomei);
            objmateriaisDto.setUniMedida(UniMedida);
            
            
            CadastroMateriaisDAO materialDao = new CadastroMateriaisDAO();
            
            materialDao.cadastroMateriais(objmateriaisDto);
            
            
            JOptionPane.showMessageDialog(null, "cadastro realizado com sucesso!");
           
        } 
        
        
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null, "Erro! Cadastro n√£o realizado, confira se todos os campos foram preenchidos view " + e);
        
            
        }
        
      
        
    }//GEN-LAST:event_CadastroNovoMaterialBtnActionPerformed

